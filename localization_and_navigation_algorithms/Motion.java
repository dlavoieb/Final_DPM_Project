

/**
 * Possible movements
 * @author Daniel Macario
 */
public enum Motion {
    /**
     * rotate 90 degree counter-clockwise
     */
    ROTATE,
    /**
     * rotate 90 degrees clock-wise
     */
    ROTATECW
    ,
    /**
     * Travel to the next tile in the current direction
     */
    FORWARD
}
